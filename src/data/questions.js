// 軸スコアの計算
export function calculateAxisScores(answers) {
  // 初期化
  let scores = {
    specialist: 0,  // 専門的（S）vs 総合的（G）
    innovative: 0,  // 革新的（I）vs 保守的（C）
    analytical: 0,  // 分析的（A）vs 実践的（P）
    human: 0        // 人間中心（H）vs 技術中心（T）
  };
  
  // 各回答の値を該当する軸に加算
  questions.forEach((question, index) => {
    const answerValue = parseFloat(answers[index]);
    
    if (!isNaN(answerValue)) {
      scores[question.axis] += answerValue * question.weight;
    }
  });
  
  return scores;
}

// 軸スコアからタイプを決定
export function determineType(scores) {
  // 各軸の閾値を決定
  const thresholds = {
    specialist: 0,  // プラスなら専門的（S）、マイナスなら総合的（G）
    innovative: 0,  // プラスなら革新的（I）、マイナスなら保守的（C）
    analytical: 0,  // プラスなら分析的（A）、マイナスなら実践的（P）
    human: 0        // プラスなら人間中心（H）、マイナスなら技術中心（T）
  };
  
  // 各軸を評価してタイプコードを作成
  let typeCode = '';
  typeCode += scores.specialist > thresholds.specialist ? 'S' : 'G';
  typeCode += scores.innovative > thresholds.innovative ? 'I' : 'C';
  typeCode += scores.human > thresholds.human ? 'H' : 'T';
  typeCode += scores.analytical > thresholds.analytical ? 'A' : 'P';
  
  return typeCode;
}

